// DELETE SINGLE ITEM FROM FIREBASE DB

const deleteItem = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/items/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

export {
  getItems,
  getItemsByOrderId,
  createItem,
  updateItem,
  deleteItem,
  getSingleItem,
};
